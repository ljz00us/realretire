var acceptable = [];


var filter_cost = function(cutoff_cost){
    cost_list = data.cities;
    list_length = cost_list.length;
    for(var i = 0; i < list_length; i++){
        curr_city = cost_list[i];
        if(curr_city.nomadCost.USD <= cutoff_cost){
            acceptable.push(curr_city);
        }
    }
    return acceptable;
}

